package com.exercito;

public class ForcaDefesa {
    private int forca;
    private int defesa;

    public ForcaDefesa(int forca, int defesa) {
        this.forca = forca;
        this.defesa = defesa;
    }

    public int getForca() {
        return forca;
    }

    public void setForca(int forca) {
        this.forca = forca;
    }

    public int getDefesa() {
        return defesa;
    }

    public void setDefesa(int defesa) {
        this.defesa = defesa;
    }
}
